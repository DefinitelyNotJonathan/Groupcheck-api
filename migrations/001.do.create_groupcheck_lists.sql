CREATE TABLE groupcheck_users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    firstname TEXT NOT NULL,
    lastname TEXT NOT NULL,
    email TEXT NOT NULL,
    date_created TIMESTAMP DEFAULT now() NOT NULL
);

CREATE TABLE groupcheck_login (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    email INTEGER REFERENCES groupcheck_users(email) NOT NULL,
    password TEXT NOT NULL
);

CREATE TABLE groupcheck_lists (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    date_published TIMESTAMP DEFAULT now() NOT NULL,
    author INTEGER REFERENCES groupcheck_users(id) ON DELETE CASCADE NOT NULL
);

CREATE TABLE groupcheck_items (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    content TEXT NOT NULL,
    priority NUMERIC NOT NULL,
    list_id INTEGER REFERENCES groupcheck_lists(id) ON DELETE CASCADE NOT NULL,
    user_id INTEGER REFERENCES groupcheck_users(id) ON DELETE CASCADE NOT NULL
);

CREATE TABLE groupcheck_users_lists (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER REFERENCES groupcheck_users(id) ON DELETE CASCADE NOT NULL,
    list_id INTEGER REFERENCES groupcheck_lists(id) ON DELETE CASCADE NOT NULL
);
